package org.h4x0r.data;

public class ConvertConfig {
    private String sourceFileUrl;
    private String destFileUrl;
    private String sourceEncode;
    private String destEncode;
    private String filter;
   
    public String getSourceFileUrl() {
        return sourceFileUrl;
    }
    public void setSourceFileUrl(String sourceFileUrl) {
        this.sourceFileUrl = sourceFileUrl;
    }
    public String getDestFileUrl() {
        return destFileUrl;
    }
    public void setDestFileUrl(String destFileUrl) {
        this.destFileUrl = destFileUrl;
    }
    public String getSourceEncode() {
        return sourceEncode;
    }
    public void setSourceEncode(String sourceEncode) {
        this.sourceEncode = sourceEncode;
    }
    public String getDestEncode() {
        return destEncode;
    }
    public void setDestEncode(String destEncode) {
        this.destEncode = destEncode;
    }
    public String getFilter() {
        return filter;
    }
    public void setFilter(String filter) {
        this.filter = filter;
    }
    
    
    
}
